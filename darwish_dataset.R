## Arabic normalization function

clean_poem <- function(text){
  
  text <- as.character(text)
  
  text <- stringr::str_remove_all(text, "[\\u064B-\\u065F\\u0670]")  # diacritics
  text <- stringr::str_remove_all(text, "ـ")                         # tatweel
  text <- stringr::str_replace_all(text, "[إأآٱ]", "ا")              # normalize alif
  text <- stringr::str_replace_all(text, "ى", "ي")                   # normalize ya
  text <- stringr::str_replace_all(text, "ة", "ه")                   # normalize ta marbuta
  text <- stringr::str_replace_all(text, "ؤ", "و")
  text <- stringr::str_replace_all(text, "ئ", "ي")
  
  text <- stringr::str_replace_all(text, "[ ]{2,}", " ")             # remove extra spaces
  
  return(text)
}


