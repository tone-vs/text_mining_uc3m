## Convert poems into line-level dataset


darwish_lines <- darwish_poems |>
  
  mutate(lines = map(text, ~ str_split(.x, "\n")[[1]])) |>
  
  unnest(lines) |>
  
  rename(line = lines) |>
  
  group_by(poem) |>
  
  mutate(line_number = row_number()) |>
  
  ungroup() |>
  
  select(poem_id, poet, poem, line_number, line)


